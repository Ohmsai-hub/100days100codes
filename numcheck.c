//Code to check if number is positive , negative or zero
#include <stdio.h>
int main() {
float num ;
printf("Enter the number: ");
scanf("%f" , &num);
if (num < 0) {
printf("The number is %f negative number.\n", num);
}
else if (num = 0) {;
printf("The number is 0. \n");
}
else if (num > 0) {;
printf("The number is %f positive number.\n" , num);
}
else {
printf("Ivalid Command");
}
return 0;
}

